// RunValidation is a legacy alias that runs scorers from a CompletionConfig.
// Deprecated: Use RunCompletionScorers instead.
func RunValidation(
	ctx context.Context,
	config CompletionConfig,
	compCtx CompletionContext,
) (CompletionRunResult, error) {
	parallel := true
	if config.Parallel != nil {
		parallel = *config.Parallel
	}
	result := RunCompletionScorers(ctx, config.Scorers, compCtx, &CompletionScorerOptions{
		Strategy: config.Strategy,
		Parallel: parallel,
		Timeout:  config.Timeout,
	})
	if config.OnComplete != nil {
		if err := config.OnComplete(result); err != nil {
			return result, err
		}
	}
	return result, nil
}

