type StreamCompletionContext struct {
	// Iteration is the current iteration number (1-based).
	Iteration int `json:"iteration"`
	// MaxIterations is the maximum iterations allowed (maxSteps).
	MaxIterations int `json:"maxIterations,omitempty"`
	// OriginalTask is the original user message/task that started this execution.
	OriginalTask string `json:"originalTask"`
	// CurrentText is the current output text from the LLM.
	CurrentText string `json:"currentText"`
	// ToolCalls made in this iteration.
	ToolCalls []ToolCallInfo `json:"toolCalls"`
	// ToolResults from this iteration.
	ToolResults []ToolResultInfo `json:"toolResults"`
	// RunID is the ID of the current run.
	RunID string `json:"runId"`
	// ThreadID is the current thread ID (if using memory).
	ThreadID string `json:"threadId,omitempty"`
	// ResourceID is the resource ID (if using memory).
	ResourceID string `json:"resourceId,omitempty"`
	// AgentID is the agent identifier.
	AgentID string `json:"agentId,omitempty"`
	// AgentName is the agent's display name.
	AgentName string `json:"agentName,omitempty"`
	// CustomContext is custom context from the request.
	CustomContext map[string]any `json:"customContext,omitempty"`
	// Messages is the full message list.
	Messages []MastraDBMessage `json:"messages"`
}

// ToolCallInfo describes a tool call made during an iteration.
type ToolCallInfo struct {
	Name string         `json:"name"`
	Args map[string]any `json:"args"`
}

// ToolResultInfo describes a tool result from an iteration.
type ToolResultInfo struct {
	Name   string `json:"name"`
	Result any    `json:"result"`
}

// RunStreamCompletionScorers runs completion scorers for stream/generate
// execution. It adapts StreamCompletionContext to work with existing scorers.
func RunStreamCompletionScorers(
	ctx context.Context,
	scorers []MastraScorer,
	sCtx StreamCompletionContext,
	options *CompletionScorerOptions,
) CompletionRunResult {
	// Adapt StreamCompletionContext to CompletionContext for scorer compatibility
	adaptedCtx := CompletionContext{
		Iteration:     sCtx.Iteration,
		MaxIterations: sCtx.MaxIterations,
		Messages:      sCtx.Messages,
		OriginalTask:  sCtx.OriginalTask,
		SelectedPrimitive: SelectedPrimitiveInfo{
			ID:   "stream",
			Type: "agent",
		},
		PrimitivePrompt: sCtx.OriginalTask,
		PrimitiveResult: sCtx.CurrentText,
		NetworkName:     sCtx.AgentName,
		RunID:           sCtx.RunID,
		ThreadID:        sCtx.ThreadID,
		ResourceID:      sCtx.ResourceID,
		CustomContext: map[string]any{
			"toolCalls":   sCtx.ToolCalls,
			"toolResults": sCtx.ToolResults,
			"agentId":     sCtx.AgentID,
			"agentName":   sCtx.AgentName,
		},
	}

	if adaptedCtx.NetworkName == "" {
		adaptedCtx.NetworkName = sCtx.AgentID
	}
	if adaptedCtx.NetworkName == "" {
		adaptedCtx.NetworkName = "stream"
	}

	// Merge caller's custom context
	if sCtx.CustomContext != nil {
		for k, v := range sCtx.CustomContext {
			adaptedCtx.CustomContext[k] = v
		}
	}

	return RunCompletionScorers(ctx, scorers, adaptedCtx, options)
}

// FormatStreamCompletionFeedback formats stream completion feedback for the LLM.
// Similar to FormatCompletionFeedback but tailored for stream context.
func FormatStreamCompletionFeedback(result CompletionRunResult, maxIterationReached bool) string {
	var b strings.Builder

	b.WriteString("#### Completion Check Results\n")
	b.WriteString("\n")
	if result.Complete {
		b.WriteString("Overall: COMPLETE\n")
	} else {
		b.WriteString("Overall: NOT COMPLETE\n")
	}
	fmt.Fprintf(&b, "Duration: %dms\n", result.TotalDuration)
	if result.TimedOut {
		b.WriteString("WARNING: Scoring timed out\n")
	}
	b.WriteString("\n")

	for _, scorer := range result.Scorers {
		fmt.Fprintf(&b, "**%s** (%s)\n", scorer.ScorerName, scorer.ScorerID)
		passStr := "FAILED"
		if scorer.Passed {
			passStr = "PASSED"
		}
		fmt.Fprintf(&b, "Score: %g %s\n", scorer.Score, passStr)
		if scorer.Reason != "" {
			fmt.Fprintf(&b, "Reason: %s\n", scorer.Reason)
		}
		b.WriteString("\n")
	}

	if result.Complete {
		b.WriteString("The task is complete.")
	} else if maxIterationReached {
		b.WriteString("Max iterations reached.")
	} else {
		b.WriteString("The task is not yet complete. Please continue working based on the feedback above.")
	}

	return b.String()
}
