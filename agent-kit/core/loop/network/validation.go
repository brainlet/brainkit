// FormatCompletionFeedback formats scorer results into a message for the LLM.
func FormatCompletionFeedback(result CompletionRunResult, maxIterationReached bool) string {
	var b strings.Builder

	b.WriteString("#### Completion Check Results\n")
	b.WriteString("\n")
	if result.Complete {
		b.WriteString("Overall: COMPLETE\n")
	} else {
		b.WriteString("Overall: NOT COMPLETE\n")
	}
	fmt.Fprintf(&b, "Duration: %dms\n", result.TotalDuration)
	if result.TimedOut {
		b.WriteString("WARNING: Scoring timed out\n")
	}
	b.WriteString("\n")

	for _, scorer := range result.Scorers {
		fmt.Fprintf(&b, "###### %s (%s)\n", scorer.ScorerName, scorer.ScorerID)
		passStr := "FAILED"
		if scorer.Passed {
			passStr = "PASSED"
		}
		fmt.Fprintf(&b, "Score: %g %s\n", scorer.Score, passStr)
		if scorer.Reason != "" {
			fmt.Fprintf(&b, "Reason: %s\n", scorer.Reason)
		}
		b.WriteString("\n")
	}

	if result.Complete {
		b.WriteString("\n\nThe task is complete.")
	} else if maxIterationReached {
		b.WriteString("\n\nMax iterations reached.")
	} else {
		b.WriteString("\n\nWill continue working on the task.")
	}

	return b.String()
}

