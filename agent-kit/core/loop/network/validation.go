// RunDefaultCompletionCheck runs the default LLM completion check.
// It evaluates "is this done?" and does NOT generate the final result.
//
// This is an internal function used by the network loop when no scorers
// are configured. It streams a structured output call to the agent with a
// completion prompt and returns pass/fail based on the response.
func RunDefaultCompletionCheck(
	ctx context.Context,
	agent Agent,
	compCtx CompletionContext,
	streamContext *StreamContext,
) (*ScorerResult, error) {
	start := time.Now()

	// Build compact list of completed primitives from network messages.
	var completedPrimitives []string
	for _, m := range compCtx.Messages {
		for _, part := range m.Content.Parts {
			if part.Type == "text" && strings.Contains(part.Text, `"isNetwork":true`) {
				var parsed map[string]any
				if err := json.Unmarshal([]byte(part.Text), &parsed); err == nil {
					if isNet, ok := parsed["isNetwork"].(bool); ok && isNet {
						pType, _ := parsed["primitiveType"].(string)
						pID, _ := parsed["primitiveId"].(string)
						completedPrimitives = append(completedPrimitives, fmt.Sprintf("%s %q", pType, pID))
					}
				}
			}
		}
	}

	completedSection := ""
	if len(completedPrimitives) > 0 {
		completedSection = "\n\nPrimitives already executed: " + strings.Join(completedPrimitives, ", ")
	}

	primitiveResultJSON, _ := json.Marshal(compCtx.PrimitiveResult)

	completionPrompt := fmt.Sprintf(`
    The %s %s has contributed to the task.
    This is the result: %s

    %s

    You need to evaluate if the task is complete. Pay very close attention to the SYSTEM INSTRUCTIONS for when the task is considered complete.
    Only return true if the task is complete according to the system instructions.
    Original task: %s

    If no primitive (type = 'none'), the task is complete because we can't run any primitive to further task completion.

    Also, if the %s %s has declined the tool call in its response, then the task is complete as the primitive tool-call was declined by the user.

    IMPORTANT: If the above result is from an AGENT PRIMITIVE and it is a suitable final result itself considering the original task, then finalResult should be an empty string or undefined.

    If the task is complete and the result is not from an AGENT PRIMITIVE, always generate a finalResult.
    IF the task is complete and the result is from an AGENT PRIMITIVE, but the AGENT PRIMITIVE response is not comprehensive enough to accomplish the user's original task, then generate a finalResult.

    IMPORTANT: The generated finalResult should not be the exact primitive result. You should craft a comprehensive response based on the message history.
    The finalResult field should be written in natural language.

    You must return this JSON shape:
    {
      "isComplete": boolean,
      "completionReason": string,
      "finalResult": string,
    }
  `, compCtx.SelectedPrimitive.Type, compCtx.SelectedPrimitive.ID,
		string(primitiveResultJSON),
		completedSection,
		compCtx.OriginalTask,
		compCtx.SelectedPrimitive.Type, compCtx.SelectedPrimitive.ID,
	)

	// Call the agent with structured output.
	stream, err := agent.Stream(ctx, completionPrompt, map[string]any{
		"maxSteps": 1,
		"structuredOutput": map[string]any{
			"schema": "default-completion-schema",
		},
	})
	if err != nil {
		return &ScorerResult{
			Score:      0,
			Passed:     false,
			Reason:     fmt.Sprintf("LLM completion check failed: %s", err.Error()),
			ScorerID:   "default-completion",
			ScorerName: "Default LLM Completion",
			Duration:   time.Since(start).Milliseconds(),
		}, nil
	}

	// Consume the stream.
	for range stream.FullStream {
		// Consume chunks.
	}

	// Parse the structured output.
	var output map[string]any
	if stream.Object != nil {
		if m, ok := stream.Object.(map[string]any); ok {
			output = m
		}
	}

	isComplete := false
	completionReason := ""
	finalResult := ""

	if output != nil {
		if ic, ok := output["isComplete"].(bool); ok {
			isComplete = ic
		}
		if cr, ok := output["completionReason"].(string); ok {
			completionReason = cr
		}
		if fr, ok := output["finalResult"].(string); ok {
			finalResult = fr
		}
	}

	score := float64(0)
	if isComplete {
		score = 1
	}

	return &ScorerResult{
		Score:       score,
		Passed:      isComplete,
		Reason:      completionReason,
		FinalResult: finalResult,
		ScorerID:    "default-completion",
		ScorerName:  "Default LLM Completion",
		Duration:    time.Since(start).Milliseconds(),
	}, nil
}

