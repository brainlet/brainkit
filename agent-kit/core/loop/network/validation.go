// GenerateFinalResult generates and streams the final result after custom
// scorers have passed. Unlike RunDefaultCompletionCheck, this doesn't evaluate
// completion -- it only generates the result.
func GenerateFinalResult(
	ctx context.Context,
	agent Agent,
	compCtx CompletionContext,
	streamContext *StreamContext,
) (string, error) {
	primitiveResultJSON, _ := json.Marshal(compCtx.PrimitiveResult)

	prompt := fmt.Sprintf(`
    The task has been completed successfully.
    Original task: %s

    The %s %s produced this result:
    %s

    IMPORTANT: If the above result is from an AGENT PRIMITIVE and it is a suitable final result itself considering the original task, then finalResult should be an empty string or undefined.
    You should evaluate if the above result is comprehensive enough to accomplish the user's original task.
    Otherwise, generate the finalResult object. If the result is not from an AGENT PRIMITIVE, always generate a finalResult.

    The generated finalResult should not be the exact primitive result. You should craft a comprehensive response based on the message history.
    The response should be written in natural language.

    Return JSON:
    {
      "finalResult": string,
    }
  `, compCtx.OriginalTask,
		compCtx.SelectedPrimitive.Type, compCtx.SelectedPrimitive.ID,
		string(primitiveResultJSON),
	)

	stream, err := agent.Stream(ctx, prompt, map[string]any{
		"maxSteps": 1,
		"structuredOutput": map[string]any{
			"schema": "final-result-schema",
		},
	})
	if err != nil {
		return "", fmt.Errorf("generate final result: %w", err)
	}

	// Consume the stream.
	for range stream.FullStream {
		// Consume chunks.
	}

	// Parse the structured output.
	if stream.Object != nil {
		if m, ok := stream.Object.(map[string]any); ok {
			if fr, ok := m["finalResult"].(string); ok {
				return fr, nil
			}
		}
	}

	return "", nil
}

