// CompletionContext is the runtime context passed to completion scoring.
// Available via run.input when using a completion scorer.
type CompletionContext struct {
	// Iteration is the current iteration number (1-based).
	Iteration int `json:"iteration"`
	// MaxIterations is the maximum iterations allowed.
	MaxIterations int `json:"maxIterations,omitempty"`
	// Messages is all messages in the conversation thread.
	Messages []MastraDBMessage `json:"messages"`
	// OriginalTask is the original task/prompt that started this network run.
	OriginalTask string `json:"originalTask"`
	// SelectedPrimitive is which primitive was selected this iteration.
	SelectedPrimitive SelectedPrimitiveInfo `json:"selectedPrimitive"`
	// PrimitivePrompt is the prompt/input sent to the selected primitive.
	PrimitivePrompt string `json:"primitivePrompt"`
	// PrimitiveResult is the result from the primitive execution.
	PrimitiveResult string `json:"primitiveResult"`
	// NetworkName is the name of the network/routing agent.
	NetworkName string `json:"networkName"`
	// RunID is the ID of the current run.
	RunID string `json:"runId"`
	// ThreadID is the current thread ID (if using memory).
	ThreadID string `json:"threadId,omitempty"`
	// ResourceID is the resource ID (if using memory).
	ResourceID string `json:"resourceId,omitempty"`
	// CustomContext is custom context from the request.
	CustomContext map[string]any `json:"customContext,omitempty"`
}

// SelectedPrimitiveInfo describes which primitive was selected for an iteration.
type SelectedPrimitiveInfo struct {
	ID   string `json:"id"`
	Type string `json:"type"` // "agent" | "workflow" | "tool" | "none"
}

// ScorerResult is the result of running a single scorer.
// Scorers evaluate pass/fail; they don't generate the final result.
type ScorerResult struct {
	// Score is the score (0 = failed, 1 = passed).
	Score float64 `json:"score"`
	// Passed indicates whether this scorer passed (score == 1).
	Passed bool `json:"passed"`
	// Reason from the scorer (why it passed/failed).
	Reason string `json:"reason,omitempty"`
	// ScorerID is the scorer's identifier.
	ScorerID string `json:"scorerId"`
	// ScorerName is the scorer's display name.
	ScorerName string `json:"scorerName"`
	// Duration in milliseconds.
	Duration int64 `json:"duration"`
	// FinalResult generated by the scorer (if any).
	FinalResult string `json:"finalResult,omitempty"`
}

// CompletionConfig configures network completion.
type CompletionConfig struct {
	// Scorers to run to determine if the task is complete.
	// Each scorer should return 0 (not complete) or 1 (complete).
	Scorers []MastraScorer `json:"-"`
	// Strategy is how to combine scorer results:
	//   "all" — All scorers must pass (score = 1) (default)
	//   "any" — At least one scorer must pass
	Strategy string `json:"strategy,omitempty"`
	// Timeout is the maximum time for all scorers (ms).
	// Default: 600000 (10 minutes).
	Timeout int `json:"timeout,omitempty"`
	// Parallel controls whether scorers run in parallel. Default: true.
	Parallel *bool `json:"parallel,omitempty"`
	// OnComplete is called after scorers run with results.
	OnComplete func(result CompletionRunResult) error `json:"-"`
	// SuppressFeedback suppresses the completion feedback message from being
	// saved to memory. When true, the "#### Completion Check Results" message
	// will not be persisted. Default: false.
	SuppressFeedback bool `json:"suppressFeedback,omitempty"`
}

// CompletionRunResult is the result of running completion checks.
type CompletionRunResult struct {
	// Complete indicates whether the task is complete (based on strategy).
	Complete bool `json:"complete"`
	// CompletionReason is the reason for completion/failure.
	CompletionReason string `json:"completionReason,omitempty"`
	// Scorers contains individual scorer results.
	Scorers []ScorerResult `json:"scorers"`
	// TotalDuration is the total duration of all checks in milliseconds.
	TotalDuration int64 `json:"totalDuration"`
	// TimedOut indicates whether checks timed out.
	TimedOut bool `json:"timedOut"`
}

