// runSingleScorer runs a single scorer and returns the result.
func runSingleScorer(ctx context.Context, scorer MastraScorer, compCtx CompletionContext) ScorerResult {
	start := time.Now()

	result, err := scorer.Run(ctx, ScorerRunInput{
		RunID:          compCtx.RunID,
		Input:          compCtx,
		Output:         compCtx.PrimitiveResult,
		RequestContext: compCtx.CustomContext,
	})

	if err != nil {
		return ScorerResult{
			Score:      0,
			Passed:     false,
			Reason:     fmt.Sprintf("Scorer threw an error: %s", err.Error()),
			ScorerID:   scorer.ID(),
			ScorerName: scorerName(scorer),
			Duration:   time.Since(start).Milliseconds(),
		}
	}

	score := float64(0)
	if result != nil {
		score = result.Score
	}
	reason := ""
	if result != nil {
		reason = result.Reason
	}

	return ScorerResult{
		Score:      score,
		Passed:     score == 1,
		Reason:     reason,
		ScorerID:   scorer.ID(),
		ScorerName: scorerName(scorer),
		Duration:   time.Since(start).Milliseconds(),
	}
}

func scorerName(s MastraScorer) string {
	if n := s.Name(); n != "" {
		return n
	}
	return s.ID()
}

// CompletionScorerOptions configures how scorers are run.
type CompletionScorerOptions struct {
	Strategy string // "all" | "any"
	Parallel bool
	Timeout  int // milliseconds
}

// RunCompletionScorers runs all completion scorers according to the configuration.
func RunCompletionScorers(
	ctx context.Context,
	scorers []MastraScorer,
	compCtx CompletionContext,
	options *CompletionScorerOptions,
) CompletionRunResult {
	strategy := "all"
	parallel := true
	timeout := 600000

	if options != nil {
		if options.Strategy != "" {
			strategy = options.Strategy
		}
		parallel = options.Parallel
		if options.Timeout > 0 {
			timeout = options.Timeout
		}
	}

	startTime := time.Now()
	var results []ScorerResult
	timedOut := false

	timeoutCtx, cancel := context.WithTimeout(ctx, time.Duration(timeout)*time.Millisecond)
	defer cancel()

	if parallel {
		type indexedResult struct {
			idx    int
			result ScorerResult
		}
		ch := make(chan indexedResult, len(scorers))
		var wg sync.WaitGroup

		for i, scorer := range scorers {
			wg.Add(1)
			go func(idx int, s MastraScorer) {
				defer wg.Done()
				r := runSingleScorer(timeoutCtx, s, compCtx)
				ch <- indexedResult{idx: idx, result: r}
			}(i, scorer)
		}

		// Wait for all goroutines in a separate goroutine
		go func() {
			wg.Wait()
			close(ch)
		}()

		collected := make([]ScorerResult, len(scorers))
		collectedCount := 0
		for ir := range ch {
			collected[ir.idx] = ir.result
			collectedCount++
		}

		// Check if context timed out
		if timeoutCtx.Err() != nil {
			timedOut = true
		}

		for i := 0; i < collectedCount; i++ {
			results = append(results, collected[i])
		}
	} else {
		for _, scorer := range scorers {
			if time.Since(startTime).Milliseconds() > int64(timeout) {
				timedOut = true
				break
			}

			result := runSingleScorer(timeoutCtx, scorer, compCtx)
			results = append(results, result)

			// Short-circuit
			if strategy == "all" && !result.Passed {
				break
			}
			if strategy == "any" && result.Passed {
				break
			}
		}
	}

	complete := false
	if strategy == "all" {
		complete = len(results) == len(scorers)
		if complete {
			for _, r := range results {
				if !r.Passed {
					complete = false
					break
				}
			}
		}
	} else {
		for _, r := range results {
			if r.Passed {
				complete = true
				break
			}
		}
	}

	// Get reason from first passing scorer (or first failing if none passed)
	var completionReason string
	for _, r := range results {
		if r.Passed {
			completionReason = r.Reason
			break
		}
	}
	if completionReason == "" && len(results) > 0 {
		completionReason = results[0].Reason
	}

	return CompletionRunResult{
		Complete:         complete,
		CompletionReason: completionReason,
		Scorers:          results,
		TotalDuration:    time.Since(startTime).Milliseconds(),
		TimedOut:         timedOut,
	}
}

