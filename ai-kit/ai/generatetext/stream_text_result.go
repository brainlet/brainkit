package generatetext

// StreamTextResult is a result object for accessing different stream types and additional information.
// In TypeScript, many fields are PromiseLike<T> (lazy promises resolved when stream is consumed).
// In Go, we represent these as fields populated after the stream is consumed.
type StreamTextResult struct {
	// Content is the content generated in the last step.
	Content []ContentPart

	// Text is the full text generated in the last step.
	Text string

	// Reasoning is the full reasoning generated.
	Reasoning []ReasoningOutput

	// ReasoningText is the reasoning text generated.
	ReasoningText string

	// Files are files generated by the model in the last step.
	Files []GeneratedFile

	// Sources are sources used as references in the last step.
	Sources []Source

	// ToolCalls are the tool calls executed in the last step.
	ToolCalls []ToolCall

	// StaticToolCalls are the static tool calls.
	StaticToolCalls []ToolCall

	// DynamicToolCalls are the dynamic tool calls.
	DynamicToolCalls []ToolCall

	// ToolResults are the tool results from the last step.
	ToolResults []ToolResult

	// StaticToolResults are the static tool results.
	StaticToolResults []ToolResult

	// DynamicToolResults are the dynamic tool results.
	DynamicToolResults []ToolResult

	// FinishReason is the unified finish reason.
	FinishReason FinishReason

	// RawFinishReason is the raw reason from the provider.
	RawFinishReason string

	// Usage is the token usage of the last step.
	Usage LanguageModelUsage

	// TotalUsage is the total token usage across all steps.
	TotalUsage LanguageModelUsage

	// Warnings are warnings from the model provider.
	Warnings []CallWarning

	// Steps are details for all steps.
	Steps []StepResult

	// Request is additional request information from the last step.
	Request LanguageModelRequestMetadata

	// Response is additional response information from the last step.
	Response StreamTextResponseMetadata

	// ProviderMetadata contains additional provider-specific metadata.
	ProviderMetadata ProviderMetadata

	// TextStream is a channel that yields only generated text deltas.
	TextStream <-chan string

	// FullStream is a channel that yields all events including text deltas, tool calls, etc.
	FullStream <-chan TextStreamPart

	// Output is the complete parsed output using the output specification.
	Output interface{}
}

// StreamTextResponseMetadata extends response metadata with messages.
type StreamTextResponseMetadata struct {
	LanguageModelResponseMetadata
	Messages []ResponseMessage
}

// TextStreamPart represents a part of the text stream.
// This is a large discriminated union in TypeScript.
type TextStreamPart struct {
	// Type discriminates the stream part kind.
	Type string

	// For text-start, text-delta, text-end, reasoning-start, reasoning-delta, reasoning-end
	ID   string
	Text string

	// For tool-input-start, tool-input-delta, tool-input-end
	ToolName         string
	Delta            string
	ProviderExecuted bool
	Dynamic          bool
	Title            string

	// For source
	Source *Source

	// For file
	File GeneratedFile

	// For tool-call
	ToolCall *ToolCall

	// For tool-result
	ToolResult *ToolResult

	// For tool-error
	ToolError *ToolError

	// For tool-output-denied
	ToolOutputDenied *ToolOutputDenied

	// For tool-approval-request
	ToolApprovalRequest *ToolApprovalRequestOutput

	// For start-step
	Request  *LanguageModelRequestMetadata
	Warnings []CallWarning

	// For finish-step
	Response        *LanguageModelResponseMetadata
	Usage           *LanguageModelUsage
	FinishReason    FinishReason
	RawFinishReason string

	// For finish
	TotalUsage *LanguageModelUsage

	// For abort
	Reason string

	// For error
	Error interface{}

	// For raw
	RawValue interface{}

	// Common metadata
	ProviderMetadata ProviderMetadata
}
